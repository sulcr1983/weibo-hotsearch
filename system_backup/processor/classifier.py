def classify_dimension(title: str, content: str = '') -> str:
    text = title + ' ' + (content or '')[:300]
    if not text.strip():
        return ''
    scored = []
    for dim_name, keywords in DIMENSION_RULES:
        score = sum(1 for kw in keywords if kw in text)
        if score > 0:
            scored.append((score, dim_name))
    if scored:
        scored.sort(key=lambda x: x[0], reverse=True)
        return scored[0][1]
    # V4.2: 关键词未命中，尝试品牌行为推断
    return _infer_dimension(title, content)


def _infer_dimension(title: str, content: str = '') -> str:
    """V4.2: 基于标题/内容模式推断维度，作为兜底策略"""
    text = title + ' ' + (content or '')[:100]
    # 交付/量产/新车 → 核心活动
    if any(kw in text for kw in ['交付', '量产', '新车', '下线', '投产', '预售',
                                    '上市', '发布', '车展', '发布', '亮相', '试驾']):
        return "⚙️ 核心活动"
    # 合作/出海/投资 → 投放与合作
    if any(kw in text for kw in ['合作', '签约', '出海', '投资', '供应链',
                                    '渠道', '充电站', '超充']):
        return "📤 投放与合作"
    # 降价/优惠/声明的 → 营销/公关
    if any(kw in text for kw in ['降', '优惠', '补贴', '回应', '声明']):
        return "🎨 创意营销/公关事件"
    # 代言/联名 → 明星IP
    if any(kw in text for kw in ['代言', '联名', '冠名']):
        return "🌟 明星与IP合作"
    # 车型/技术/配置 → 核心活动
    if any(kw in text for kw in ['车型', '配置', '技术', 'OTA', '智驾', '续航',
                                    '电池', '工厂', '业绩', '销量', '订单']):
        return "⚙️ 核心活动"
    return ''


def get_all_dimensions() -> List[str]:
    return [d[0] for d in DIMENSION_RULES]
